    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
            if (tblUser.getSelectedRow() >= 0 && kasir != null) {
                kasir = new FormDialogUser().editDialog(kasir);
                if (kasir != null) {
                    Main.getSecurityService().simpanKasir(kasir);
                    loadDataToTable();
                    JOptionPane.showMessageDialog(this, "Data berhasil diUpdate....",
                            "Pesan Sukses", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(Main.getMainForm(),
                        "Tidak ada data yang ingin di edit !!",
                        "Terjadi Kesalahan !!",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEditActionPerformed


