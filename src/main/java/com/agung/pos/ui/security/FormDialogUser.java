    public void loadFormToModel() {
        if (kasir == null) {
            kasir = new Kasir();
        }

        kasir.setKodeKasir(txtKode.getText());
        kasir.setNamaKasir(txtNama.getText());
        kasir.setUserName(txtUsername.getText());
        kasir.setAlamatKasir(txtAlamat.getText());
        kasir.setPassword(PasswordHelper
                .getEncryptedTextFromPlainText(
                        String.valueOf(txtPasworrd.getPassword())));
        if (rbLaki.isSelected()) {
            kasir.setJenisKelamin(JENKEL.LAKI.toString());
        } else {
            kasir.setJenisKelamin(JENKEL.PEREMPUAN.toString());
        }
    }

    public void loadModelToForm() {
        txtKode.setText(kasir.getKodeKasir());
        txtNama.setText(kasir.getNamaKasir());
        txtUsername.setText(kasir.getUserName());
        txtAlamat.setText(kasir.getAlamatKasir());
        txtPasworrd.setText(PasswordHelper
                .getPlainTextFromEncryptedText(
                        kasir.getPassword()));
        if (kasir.getJenisKelamin().equalsIgnoreCase("LAKI")) {
            rbLaki.setSelected(true);
        } else {
            rbPerempuan.setSelected(true);
        }
        System.out.println("password" + kasir.getPassword());
    }

    private Boolean validasiForm() {
        if (txtKode.getText().trim().length() > 0
                && txtNama.getText().trim().length() > 0
                && txtUsername.getText().trim().length() > 0
                && txtPasworrd.getPassword().toString().trim().length() > 0) {
            return true;
        } else {
            return false;
        }
    }

