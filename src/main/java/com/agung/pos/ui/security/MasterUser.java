    private void loadDataToTable() {
        listKasir = Main.getSecurityService().cariSemuaKasir();
        if (listKasir != null) {
            tblUser.setModel(new UserTableModel(listKasir));
        } else {
            tblUser.setModel(new UserTableModel(new ArrayList<Kasir>()));
        }
        TableUtil.initColumn(tblUser);
    }

    private class TableSelection implements ListSelectionListener {

        @Override
        public void valueChanged(ListSelectionEvent e) {
            if (e.getValueIsAdjusting()) {
                return;
            }

            if (tblUser.getSelectedRow() >= 0) {
                kasir = listKasir.get(tblUser.getSelectedRow());
                btnHapus.setEnabled(true);
                btnEdit.setEnabled(true);
            }
        }

    }

    private class UserTableModel extends AbstractTableModel {

        private String[] header = {"Kode Kasir", "Nama", "User Name"};
        private List<Kasir> listKasir = new ArrayList<Kasir>();

        public UserTableModel(List<Kasir> listKasirs) {
            this.listKasir = listKasirs;
        }

        @Override
        public int getRowCount() {
            return listKasir.size();
        }

        @Override
        public int getColumnCount() {
            return header.length;
        }

        @Override
        public String getColumnName(int column) {
            return header[column];
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Kasir k = listKasir.get(rowIndex);

            switch (columnIndex) {
                case 0:
                    return k.getKodeKasir();
                case 1:
                    return k.getNamaKasir();
                case 2:
                    return k.getUserName();
                default:
                    return "";
            }
        }

    }

