    private class ProgressReport extends SwingWorker<JasperPrint, Object> {

        protected void done() {
            try {
                if (get() != null) {
                    Main.getMainForm().getStatus().setText("Menyiapkan Laporan ....");
                    rptPanel.removeAll();
                    rptPanel.setLayout(new BorderLayout());
                    rptPanel.add(new JRViewer(get()), BorderLayout.CENTER);
                    rptPanel.repaint();

                    btnOk.setEnabled(true);
                    Main.getMainForm().getStatus().setText("");
                    Main.getMainForm().getProgress().setIndeterminate(false);
                }
            } catch (InterruptedException ex) {
                Logger.getLogger(LaporanPenjualanHarian.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ExecutionException ex) {
                Logger.getLogger(LaporanPenjualanHarian.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        @Override
        protected JasperPrint doInBackground() throws Exception {
            Date tanggal = jdcTanggalTransaksi.getDate();
            btnOk.setEnabled(false);
            Main.getMainForm().getStatus().setText("Menyiapkan Laporan ...");
            return Main.getReportService().cetakLaporanTransaksiHarian(tanggal);
        }
    }
