    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        Integer resp = JOptionPane.showConfirmDialog(this, "Apakah anda ingin menghapus data ini ?",
                "Konfirmasi", JOptionPane.YES_NO_OPTION);
        try {
            if (tblUser.getSelectedRow() >= 0 && kasir != null) {
                if (resp == 0) {

                    Main.getSecurityService().hapusKasir(kasir);
                    loadDataToTable();

                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus.",
                            "Pesan Sukses", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    kasir = null;
                    clearForm();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tidak ada data ingin dihapus.",
                        "Pesan Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data masih digunakan tidak bisa dihapus.",
                    "Pesan Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

