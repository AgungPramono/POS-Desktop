    private void txtKodeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtKodeFocusLost
        // TODO add your handling code here:
        Kasir k = Main.getSecurityService().findByKode(txtKode.getText());
        if (k != null && txtKode.getText().equalsIgnoreCase(k.getKodeKasir())) {
            JOptionPane.showMessageDialog(this,
                    "Maaf,Kode yang anda masukkan sudah digunakan !!!", "Terjadi Kesalahan",
                    JOptionPane.ERROR_MESSAGE);
            txtKode.setText("");
            txtKode.requestFocus();
        } else {
            txtNama.requestFocus();
        }
    }//GEN-LAST:event_txtKodeFocusLost


