    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        try {
            Date tanggal = jdcTanggalTransaksi.getDate();

            if (tanggal != null) {
                if (progressReport != null && !progressReport.isDone()) {
                    progressReport.cancel(true);
                    progressReport = null;
                }

                progressReport = new ProgressReport();
                progressReport.execute();

                Main.getMainForm().getStatus().setText("Persipan Laporan ....");
                Main.getMainForm().getProgress().setIndeterminate(true);
            } else {
                JOptionPane.showMessageDialog(Main.getMainForm(),
                        "Silahkan Pilih tanggal transaksi !!",
                        "Terjadi Kesalahan !!",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnOkActionPerformed

